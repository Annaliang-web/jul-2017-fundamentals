// infinite loop
let counter = 0;

while(counter < 10) {
  // code block
  console.log("hello " + counter);
  // incrementing the counter
  counter++; // ++ operand

  // counter = counter + 1;
  // counter += 1;
}

// a shorter way to write a loop
for (let counter = 0; counter < 10; counter++) {
  console.log("hello " + counter);
}

